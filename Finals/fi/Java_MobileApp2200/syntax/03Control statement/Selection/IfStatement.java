/*
 * ชื่อไฟล์: IfStatement.java
 * หัวข้อ: คำสั่งตรวจสอบเงื่อนไข if (One-way Selection)
 * คำอธิบาย: การใช้คำสั่ง if เพื่อตรวจสอบเงื่อนไขที่เป็นจริงเท่านั้น
 * ตัวอย่างการใช้งาน: แสดงข้อความแจ้งเตือนเมื่อเกิดข้อผิดพลาด, ให้ส่วนลดเมื่อยอดซื้อถึงเป้า
 */

import java.util.Scanner;

public class IfStatement {
    public static void main(String[] args) {
        System.out.println("=== คำสั่ง if (แบบทางเดียว) ===");

        Scanner scanner = new Scanner(System.in);

        System.out.print("กรุณากรอกอายุของคุณ: ");
        int age = scanner.nextInt();

        // เงื่อนไข: ถ้าอายุมากกว่าหรือเท่ากับ 18
        if (age >= 18) {
            System.out.println("คุณบรรลุนิติภาวะแล้ว");
            System.out.println("สามารถทำใบขับขี่ได้");
        }

        // คำสั่งนี้นอก if ทำงานเสมอ
        System.out.println("ขอบคุณที่ใช้บริการ");

        System.out.println("-------------------------");

        // ตัวอย่างที่ 2: if บรรทัดเดียว (ไม่ต้องใส่ปีกกา แต่ไม่แนะนำ)
        int score = 80;
        if (score >= 50)
            System.out.println("สอบผ่าน");

        scanner.close();
    }
}

