/*
 * ชื่อไฟล์: IfWithLoop.java
 * หัวข้อ: การใช้ if statement ร่วมกับ loop
 * คำอธิบาย: ตัวอย่างการผสมผสานการใช้งาน if-else กับ for loop
 *           สำหรับตรวจสอบเงื่อนไขในแต่ละรอบการวนซ้ำ
 * ตัวอย่างการใช้งาน: กรองข้อมูลตามเงื่อนไข, ค้นหาข้อมูลในอาร์เรย์
 */

public class IfWithLoop {
    public static void main(String[] args) {
        // ตัวอย่างที่ 1: นับจำนวนเลขคู่และเลขคี่ในชุดข้อมูล
        System.out.println("=== ตัวอย่างที่ 1: นับเลขคู่และเลขคี่ ===");

        int[] numbers = { 1, 2, 5, 8, 9, 12, 14, 15, 20, 21 };
        int evenCount = 0; // ตัวนับเลขคู่
        int oddCount = 0; // ตัวนับเลขคี่

        // วนลูปตรวจสอบแต่ละตัวเลข
        for (int i = 0; i < numbers.length; i++) {
            if (numbers[i] % 2 == 0) { // ถ้าหารด้วย 2 ลงตัว = เลขคู่
                evenCount++;
                System.out.println(numbers[i] + " เป็นเลขคู่");
            } else { // ถ้าไม่ลงตัว = เลขคี่
                oddCount++;
                System.out.println(numbers[i] + " เป็นเลขคี่");
            }
        }

        System.out.println("สรุป: มีเลขคู่ " + evenCount + " ตัว, เลขคี่ " + oddCount + " ตัว");

        // ตัวอย่างที่ 2: หาผลรวมเฉพาะตัวเลขที่หารด้วย 3 ลงตัว
        System.out.println("\n=== ตัวอย่างที่ 2: ผลรวมของเลขที่หาร 3 ลงตัว (1-20) ===");
        int sum = 0;

        for (int i = 1; i <= 20; i++) {
            if (i % 3 == 0) {
                System.out.print(i + " "); // แสดงเลขที่หารลงตัว
                sum += i; // บวกค่าเพิ่ม
            }
        }
        System.out.println("\nผลรวมคือ: " + sum);
    }
}

